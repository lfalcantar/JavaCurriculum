package com.gm.tutorial.basics;

/**
 * while loop
 */
public class WhileLoop {

    public static void main(String[] args) {
        // initialization
        int i = 0;

        // check
        while(i < 15) {
            // body
            System.out.println(i);

            //update
            i = i + 1;
        }
    }
}
