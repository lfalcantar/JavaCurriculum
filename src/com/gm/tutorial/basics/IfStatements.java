package com.gm.tutorial.basics;

/**
 * conditions
 */
public class IfStatements {

    public static void main(String[] args) {

        int counter = 10;
        if(counter < 10) {
            System.out.println("less than 10");
        }

        if(counter == 10) {
            System.out.println("equal than 10");
        }

        if(counter > 10) {
            System.out.println("bigger than 10");
        }

    }
}
